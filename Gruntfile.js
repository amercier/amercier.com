function htmlentities (string, quoteStyle, charset, doubleEncode) {
  // http://kevin.vanzonneveld.net
  // +   original by: Kevin van Zonneveld (http://kevin.vanzonneveld.net)
  // +    revised by: Kevin van Zonneveld (http://kevin.vanzonneveld.net)
  // +   improved by: nobbler
  // +    tweaked by: Jack
  // +   bugfixed by: Onno Marsman
  // +    revised by: Kevin van Zonneveld (http://kevin.vanzonneveld.net)
  // +    bugfixed by: Brett Zamir (http://brett-zamir.me)
  // +      input by: Ratheous
  // +   improved by: Rafa≈Ç Kukawski (http://blog.kukawski.pl)
  // +   improved by: Dj (http://phpjs.org/functions/htmlentities:425#comment_134018)
  // -    depends on: getHtmlTranslationTable
  // *     example 1: htmlentities('Kevin & van Zonneveld');
  // *     returns 1: 'Kevin &amp; van Zonneveld'
  // *     example 2: htmlentities('foo'bar','ENT_QUOTES');
  // *     returns 2: 'foo&#039;bar'
  var hashMap = getHtmlTranslationTable('HTML_ENTITIES', quoteStyle);
  string = string === undefined ? '' : string + '';

  if (!hashMap) {
    return false;
  }

  if (quoteStyle && quoteStyle === 'ENT_QUOTES') {
    hashMap['"'] = '&#039;';
  }

  if (!!doubleEncode || doubleEncode === undefined) {
    for (var symbol in hashMap) {
      if (hashMap.hasOwnProperty(symbol)) {
        string = string.split(symbol).join(hashMap[symbol]);
      }
    }
  } else {
    string = string.replace(/([\s\S]*?)(&(?:#\d+|#x[\da-f]+|[a-zA-Z][\da-z]*);|$)/g, function (ignore, text, entity) {
      for (var symbol in hashMap) {
        if (hashMap.hasOwnProperty(symbol)) {
          text = text.split(symbol).join(hashMap[symbol]);
        }
      }

      return text + entity;
    });
  }

  return string;
}

